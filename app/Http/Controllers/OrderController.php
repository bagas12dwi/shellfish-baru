     */
    public function index()
    {

        $user = auth()->user();
        $count = Cart::where('phone', $user->phone)->count();
        $jumlah = Cart::where('phone', $user->phone)->sum('price');
        $cart = Cart::where('phone', $user->phone)->get();


        return view('user.checkout', [
            'title' => "Sellfish - Confirm Payment"
        ])->with('count', $count)->with('jumlah', $jumlah)->with('cart', $cart);
    }

